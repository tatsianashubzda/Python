#–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—ë—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤—Å—Ç–∞–≤–ª—è–µ—Ç –≤ –∑–∞–¥–∞–Ω–Ω–æ–µ —á–∏—Å–ª–æ –∑–∞–ø—è—Ç—ã–µ –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å–æ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–º –∞–º–µ—Ä–∏–∫–∞–Ω—Å–∫–∏–º —Å–æ–≥–ª–∞—à–µ–Ω–∏–µ–º –æ –∑–∞–ø—è—Ç—ã—Ö –≤ –±–æ–ª—å—à–∏—Ö —á–∏—Å–ª–∞—Ö.



n = int(input())

# –ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ —á–∏—Å–ª–∞ –≤ —Å—Ç—Ä–æ–∫—É
s = str(n)

# –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –¥–ª–∏–Ω—ã —Å—Ç—Ä–æ–∫–∏
l = len(s)

# –†–∞—Å—á—ë—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –∑–∞–ø—è—Ç—ã—Ö
comma_count = (l - 1) // 3

# –í—Å—Ç–∞–≤–∫–∞ –∑–∞–ø—è—Ç—ã—Ö
for i in range(comma_count):
    comma_index = l - (i + 1) * 3
    s = s[:comma_index] + ',' + s[comma_index:]

print(s)




