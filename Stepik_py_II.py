print("–¢—Ä–µ—Ç—å—è —á–µ—Ç–≤–µ—Ä—Ç—å:", q3)
print("–ß–µ—Ç–≤–µ—Ä—Ç–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å:", q4)




