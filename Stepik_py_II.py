#–ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –£—á—Ç–∏—Ç–µ, —á—Ç–æ —Ç–æ—á–∫–∏, –ª–µ–∂–∞—â–∏–µ –Ω–∞ –æ—Å—è—Ö –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç, –Ω–µ –ø—Ä–∏–Ω—è—Ç–æ –æ—Ç–Ω–æ—Å–∏—Ç—å –∫ –∫–∞–∫–æ–π-–ª–∏–±–æ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–Ω–æ–π —á–µ—Ç–≤–µ—Ä—Ç–∏.

n = int(input())
q1, q2, q3, q4 = 0, 0, 0, 0
for i in range(n):
    x, y = map(int, input().split())
    if x > 0 and y > 0:
        q1 += 1
    elif x < 0 and y > 0:
        q2 += 1
    elif x < 0 and y < 0:
        q3 += 1
    elif x > 0 and y < 0:
        q4 += 1

# –≤—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
print("–ü–µ—Ä–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å:", q1)
