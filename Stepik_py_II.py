#x, –∑–∞—Ç–µ–º –æ—Ä–¥–∏–Ω–∞—Ç–∞ ‚Äì y), —Ä–∞–∑–¥–µ–ª–µ–Ω–Ω—ã—Ö —Å–∏–º–≤–æ–ª–æ–º –ø—Ä–æ–±–µ–ª–∞.
